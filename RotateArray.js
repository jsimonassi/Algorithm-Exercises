/**
 * @param {number[]} nums
 * @param {number} k
 * @return {void} Do not return anything, modify nums in-place instead.
 */
var rotate = function(nums, k) {
    const arrayLength = nums.length;

    let kPoint = arrayLength - k;
    let endPoint = arrayLength - 1;

    for(let i = kPoint; i < arrayLength; i++){
        const temp = nums[i];
        nums[i] = nums[endPoint];
        nums[endPoint] = temp;
        endPoint--;
    }
};

const nums = [1,2,3,4,5,6,7];
console.log("rotate: ", rotate(nums, 3));